# Teoria: booleanos

True == 1 
False == 0

# Operadores logicos
# AND && and
print(True and True)   # 1
print(True and False)  # 0
print(False and True)  # 0
print(False and False) # 0
print("-------------")
# OR | or

print(True or True)   # 1
print(True or False)  # 1
print(False or True)  # 1
print(False or False) # 0
print("-------------")

# NOT not 
print(not True)  # 0
print(not False) # 1