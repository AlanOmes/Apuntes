# Teoría: tipos de datos

# String -> cadena de textos -> str
print ("¿Que tipo de dato es 'hola'?", type("hola"))

# Integer -> numeros enteros
# int
numero = 243
print ("¿Que tipo de dato es '243'?", type (numero))

# Flotante -> numeros con decimales
# float

numero_float = 23.1
print ("¿Que tipo de dato es 23.1?", type(numero_float))

# Booleano -> verdaderos o falsos -> bool
# True = es verdadero
# False = es falso
# True = 1
# False = 0

booleano = True
print("¿Que tipo de dato es True", type(booleano))